function clear_level () {
    for (let value of sprites.allOfKind(SpriteKind.Player)) {
        value.destroy()
    }
    for (let value of sprites.allOfKind(SpriteKind.Level_clear)) {
        value.destroy()
    }
    level += 1
    create_level()
}
sprites.onOverlap(SpriteKind.Player, SpriteKind.Level_clear, function (sprite, otherSprite) {
    clear_level()
})
